export class Coordinates {

	x: number = 0;
	y: number = 0;

	public constructor (x: number, y: number) {
		this.x = x;
		this.y = y;
	}// constructor;

}// Coordinates;


export class Dimensions {

	width: number = 0;
	height: number = 0;

	public constructor (x: number, y: number) {
		this.width = x;
		this.height = y;
	}// constructor;

}// Dimensions;